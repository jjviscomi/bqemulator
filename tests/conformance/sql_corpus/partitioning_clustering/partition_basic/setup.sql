PARTITION BY order_date AS
  SELECT 1 AS id, NUMERIC "100.00" AS amount, DATE "2024-01-15" AS order_date UNION ALL
  SELECT 2, NUMERIC "200.00", DATE "2024-01-16" UNION ALL
  SELECT 3, NUMERIC "300.00", DATE "2024-01-17" UNION ALL
  SELECT 4, NUMERIC "400.00", DATE "2024-02-01";

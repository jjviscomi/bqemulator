CREATE OR REPLACE TABLE `${DATASET}.hourly_events`
PARTITION BY TIMESTAMP_TRUNC(event_ts, HOUR)
AS
  SELECT 1 AS id, TIMESTAMP "2024-01-15 09:00:00+00" AS event_ts UNION ALL
  SELECT 2 AS id, TIMESTAMP "2024-01-15 09:30:00+00" AS event_ts UNION ALL
  SELECT 3 AS id, TIMESTAMP "2024-01-15 10:00:00+00" AS event_ts UNION ALL
  SELECT 4 AS id, TIMESTAMP "2024-01-15 11:00:00+00" AS event_ts;
